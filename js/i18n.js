/**
 * Internationalization (i18n) Module
 * Supports: English, Spanish, French, German, Chinese, Japanese, Russian, Turkish
 */
class I18n {
    constructor() {
        this.currentLang = localStorage.getItem('cpu_language') || 'en';
        this.currentCurrency = localStorage.getItem('cpu_currency') || 'USD';
        this.listeners = [];
    }

    t(key) {
        const lang = I18n.translations[this.currentLang] || I18n.translations['en'];
        return lang[key] || I18n.translations['en'][key] || key;
    }

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('cpu_language', lang);
        document.documentElement.lang = lang;
        this.notify();
    }

    setCurrency(code) {
        this.currentCurrency = code;
        localStorage.setItem('cpu_currency', code);
        this.notify();
    }

    subscribe(callback) {
        this.listeners.push(callback);
    }

    notify() {
        this.listeners.forEach(cb => cb(this.currentLang));
    }
}

